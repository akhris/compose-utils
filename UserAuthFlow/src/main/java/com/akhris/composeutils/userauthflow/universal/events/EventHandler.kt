package com.akhris.composeutils.userauthflow.universal.events


interface IEventHandler {
    suspend fun handleEvent(event: Event): EventResult
}

sealed class EventResult {
    class Success(val sourceEvent: Event, val result: Result) : EventResult()
    class Failure(val sourceEvent: Event, val exception: Exception) : EventResult()
}

sealed class Result
